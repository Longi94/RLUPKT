    public class RLDecryptor
    {
        public ICryptoTransform GetCryptoTransform(byte[] Key)
        {
            var RLDecrptor = new RijndaelManaged
            {
                KeySize = 256,
                Key = Key,
                Mode = CipherMode.ECB,
                Padding = PaddingMode.None
            };

            return RLDecrptor.CreateDecryptor();
        }
